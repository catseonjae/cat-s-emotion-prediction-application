typealias LumaListener = (luma: Double) -> Unit


class MainActivity : AppCompatActivity() {
    private class LuminosityAnalyzer(private val listener: LumaListener) : ImageAnalysis.Analyzer {

        private fun ByteBuffer.toByteArray(): ByteArray {
            rewind()    // Rewind the buffer to zero
            val data = ByteArray(remaining())
            get(data)   // Copy the buffer into a byte array
            return data // Return the byte array
        }

        override fun analyze(image: ImageProxy) {

            val buffer = image.planes[0].buffer
            val data = buffer.toByteArray()
            val pixels = data.map { it.toInt() and 0xFF }
            val luma = pixels.average()

            listener(luma)

            image.close()
        }
    }
    private class SkeletonAnalyzer(private val stream: ArrayList<ArrayList<ArrayList<Float>>>) :ImageAnalysis.Analyzer{

        private fun ByteBuffer.toByteArray(): ByteArray {
            rewind()    // Rewind the buffer to zero
            val data = ByteArray(remaining())
            get(data)   // Copy the buffer into a byte array
            return data // Return the byte array
        }
        @ExperimentalGetImage
        private fun callSkeletonModel(imageProxy: ImageProxy):ArrayList<ArrayList<Float>> {
            var image = imageProxy.image
            var plane = image!!.planes
            var frame = ArrayList<ArrayList<Float>>()
            for(i:Plane in plane){
                var buffer = i.buffer
                var floatBuffer = buffer.asFloatBuffer()
                var line = floatBuffer.array()
                frame.add(line.toCollection(ArrayList<Float>() ) )
            }

            return ArrayList<ArrayList<Float>>()
        }
        @ExperimentalGetImage
        override fun analyze(imageProxy: ImageProxy) {
            var skeleton :ArrayList<ArrayList<Float>> = callSkeletonModel(imageProxy)
            stream.add(skeleton)
            imageProxy.close()
        }
    }
    private lateinit var viewBinding: ActivityMainBinding

    private var imageCapture: ImageCapture? = null

    private var videoCapture: VideoCapture<Recorder>? = null
    private var recording: Recording? = null

    private lateinit var cameraExecutor: ExecutorService

